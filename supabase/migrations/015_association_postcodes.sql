-- Phase 2: postcode → association mapping (starter seed; extend with more rows as needed)

CREATE TABLE association_postcodes (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  association_id UUID NOT NULL REFERENCES associations(id) ON DELETE CASCADE,
  postcode TEXT NOT NULL,
  suburb TEXT,
  state TEXT NOT NULL DEFAULT 'NSW',
  created_at TIMESTAMPTZ DEFAULT NOW() NOT NULL
);

CREATE INDEX idx_association_postcodes_postcode ON association_postcodes(postcode);
CREATE INDEX idx_association_postcodes_association_id ON association_postcodes(association_id);

ALTER TABLE player_profiles
  ADD COLUMN IF NOT EXISTS suburb TEXT;

ALTER TABLE coach_profiles
  ADD COLUMN IF NOT EXISTS suburb TEXT;

